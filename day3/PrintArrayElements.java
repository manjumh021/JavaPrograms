// Java Program to print the elements of an array

public class PrintArrayElements {
    public static void main(String[] args) {
        int[] myList = {1, 2, 3, 4};
        // Print all the array elements
        for (int i = 0; i < myList.length; i++) {
            System.out.println(myList[i] + " ");
        }
    }
}